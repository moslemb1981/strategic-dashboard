class Command(BaseCommand):
    help = "نمونه‌های قبلی PESTEL را پاک و تحلیل واقعی PESTEL سایپا یدک را جایگزین می‌کند."

    def handle(self, *args, **options):
        PestelFactor.objects.all().delete()
        total = 0
        for category, kinds in DATA.items():
            for kind, items in kinds.items():
                for order, text in enumerate(items, start=1):
                    PestelFactor.objects.create(category=category, kind=kind, text=text, order=order)
                    total += 1
        self.stdout.write(self.style.SUCCESS(f"PESTEL نمونه پاک شد. {total} مورد واقعی جایگزین شد."))
