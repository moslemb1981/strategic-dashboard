class Command(BaseCommand):
    help = "نمونه‌های قبلی SWOT/TOWS را پاک و SWOT واقعی سناریوی «هوای مه‌آلود» سایپا یدک را جایگزین می‌کند."

    def handle(self, *args, **options):
        SWOTItem.objects.all().delete()
        TOWSStrategy.objects.all().delete()

        for text in STRENGTHS:
            SWOTItem.objects.create(category="s", text=text, weight=3, impact="med")
        for text in WEAKNESSES:
            SWOTItem.objects.create(category="w", text=text, weight=3, impact="med")
        for text in OPPORTUNITIES:
            SWOTItem.objects.create(category="o", text=text, weight=3, impact="med")
        for text in THREATS:
            SWOTItem.objects.create(category="t", text=text, weight=3, impact="med")

        for order, text in enumerate(SO, start=1):
            TOWSStrategy.objects.create(category="so", text=text, order=order)
        for order, text in enumerate(ST, start=1):
            TOWSStrategy.objects.create(category="st", text=text, order=order)
        for order, text in enumerate(WO, start=1):
            TOWSStrategy.objects.create(category="wo", text=text, order=order)
        for order, text in enumerate(WT, start=1):
            TOWSStrategy.objects.create(category="wt", text=text, order=order)

        self.stdout.write(self.style.SUCCESS(
            "SWOT نمونه پاک شد. SWOT واقعی سناریو «هوای مه‌آلود» جایگزین شد: "
            f"{len(STRENGTHS)} قوت، {len(WEAKNESSES)} ضعف، {len(OPPORTUNITIES)} فرصت، {len(THREATS)} تهدید، "
            f"{len(SO)+len(ST)+len(WO)+len(WT)} راهبرد TOWS."
        ))
        self.stdout.write(self.style.WARNING(
            "وزن اهمیت همه موارد روی ۳ (خنثی) تنظیم شده چون فایل منبع رتبه‌بندی نداشت — "
            "از پنل ادمین یا مداد ویرایش، وزن واقعی هر مورد را اصلاح کنید."
        ))
