class Command(BaseCommand):
    help = "داده‌ی نمونه SWOT و راهبردهای TOWS متناسب با سایپا یدک را ثبت می‌کند (اجرای دوباره، تکراری نمی‌سازد)."

    def handle(self, *args, **options):
        created_swot = 0
        for category, text, weight in SWOT_ITEMS:
            obj, was_created = SWOTItem.objects.get_or_create(
                category=category, text=text, defaults={"weight": weight, "impact": "high" if weight >= 4 else "med"},
            )
            if was_created:
                created_swot += 1

        created_tows = 0
        for category, text, order in TOWS_ITEMS:
            obj, was_created = TOWSStrategy.objects.get_or_create(
                category=category, text=text, defaults={"order": order},
            )
            if was_created:
                created_tows += 1

        self.stdout.write(self.style.SUCCESS(
            f"{created_swot} مورد SWOT جدید (از ۲۰ مورد نمونه) و {created_tows} راهبرد TOWS جدید (از ۱۲ مورد نمونه) ثبت شد."
        ))
