class Command(BaseCommand):
    help = "چشم‌انداز، مأموریت، ارزش‌های سازمانی و خط‌مشی کیفیت واقعی سایپا یدک را ثبت/به‌روزرسانی می‌کند."

    def handle(self, *args, **options):
        OrgIdentity.objects.update_or_create(
            pk=1,
            defaults=dict(
                vision=VISION, mission=MISSION,
                signed_by="رحیم علیشاهی", signed_role="مدیرعامل", signed_date="۱۴۰۵/۰۳/۲۲",
            ),
        )

        OrgValue.objects.all().delete()
        for order, (text, is_center) in enumerate(VALUES):
            OrgValue.objects.create(text=text, is_center=is_center, order=order)

        QualityPolicyPoint.objects.all().delete()
        for order, (number, text) in enumerate(POLICY_POINTS):
            QualityPolicyPoint.objects.create(number=number, text=text, order=order)

        self.stdout.write(self.style.SUCCESS(
            f"ثبت شد: چشم‌انداز/مأموریت، {len(VALUES)} ارزش سازمانی، {len(POLICY_POINTS)} بند خط‌مشی کیفیت."
        ))
