class Command(BaseCommand):
    help = "ده ریسک نمونه (با امتیاز ذاتی/باقیمانده/هدف) متناسب با سایپا یدک ثبت می‌کند (اجرای دوباره، تکراری نمی‌سازد)."

    def handle(self, *args, **options):
        created = 0
        for (title, owner, category, il, ii, l, i, tl, ti,
             response, trend, kri, mitigation) in RISKS:
            obj, was_created = Risk.objects.get_or_create(
                title=title,
                defaults=dict(
                    owner=owner, category=category,
                    inherent_likelihood=il, inherent_impact=ii,
                    likelihood=l, impact=i,
                    target_likelihood=tl, target_impact=ti,
                    response_strategy=response, trend=trend, kri=kri, mitigation=mitigation,
                ),
            )
            if was_created:
                created += 1
        self.stdout.write(self.style.SUCCESS(f"{created} ریسک جدید ثبت شد (از ۱۰ مورد نمونه)."))
